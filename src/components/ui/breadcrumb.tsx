export interface BreadcrumbSegment {
  label: string;
  path: string;
  isLast?: boolean;
}

export interface BreadcrumbProps {
  /**
   * Manual segments to display (overrides auto-generated segments)
   */
  segments?: BreadcrumbSegment[];
  /**
   * Show home icon as first segment
   */
  showHome?: boolean;
  /**
   * Maximum number of segments to show before truncating
   */
  maxSegments?: number;
  /**
   * Custom style for the container
   */
  style?: ViewStyle;
  /**
   * Callback when a segment is pressed
   */
  onSegmentPress?: (segment: BreadcrumbSegment) => void;
}

